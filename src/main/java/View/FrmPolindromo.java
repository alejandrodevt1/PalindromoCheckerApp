    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
       
        String palabra = txtPalabra.getText();
        
        if(verificarCampo(palabra)){
            JOptionPane.showMessageDialog(null, "El campo no puede estar vacio", "Error",JOptionPane.ERROR_MESSAGE);
        }else{
            lblResultado.setVisible(true);
            String palabraNormalizada = _cPalindromo.normalizarTexto(palabra);
            System.out.println(palabraNormalizada);
            char[] palabraArray = palabraNormalizada.toCharArray();
            String palabraInvertida = _cPalindromo.obtenerPalabraInvertida(palabraArray);
            
            if(_cPalindromo.verificarPalindromo(palabraNormalizada, palabraInvertida)){
                lblResultado.setText("Es Palindromo");
            }else{
                lblResultado.setText("La palabra no es Palindromo");
            }
            
        }
        
        
    }//GEN-LAST:event_btnVerificarActionPerformed

    public boolean verificarCampo(String palabra){
        if(palabra.isBlank()|| palabra.isEmpty()){
            return true;
        }
        return false;
    }
